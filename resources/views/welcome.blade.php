    <script>
        // Define la fecha objetivo (9 de marzo a las 8 pm en UTC)
        const countdownDate = new Date("2024-04-27T20:00:00Z").getTime();

        // Actualiza la cuenta regresiva cada segundo
        const countdown = setInterval(function() {
            const now = new Date().getTime();
            const distance = countdownDate - now;

            // Calcula los días, horas, minutos y segundos restantes
            const days = Math.floor(distance / (1000 * 60 * 60 * 24));
            const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
            const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
            const seconds = Math.floor((distance % (1000 * 60)) / 1000);

            // Actualiza los elementos HTML con los valores calculados
            document.getElementById("days").innerHTML = days.toString().padStart(2, '0');
            document.getElementById("hours").innerHTML = hours.toString().padStart(2, '0');
            document.getElementById("minutes").innerHTML = minutes.toString().padStart(2, '0');
            document.getElementById("seconds").innerHTML = seconds.toString().padStart(2, '0');

            // Si la cuenta regresiva ha terminado, muestra un mensaje o realiza alguna acción
            if (distance < 0) {
                clearInterval(countdown);
                document.getElementById("countdown").innerHTML = "¡La cuenta regresiva ha terminado!";
            }
        }, 1000);

        
        
        // Obtén el contenedor de frases
        const phrasesContainer = document.getElementById('phrases-container');

        // Obtén todas las frases dentro del contenedor
        const phrases = phrasesContainer.querySelectorAll('.phrase');

        // Función para mostrar las frases de forma aleatoria
        function showRandomPhrases() {
            // Oculta todas las frases
            phrases.forEach((phrase) => {
                phrase.style.display = 'none';
            });

            // Genera un índice aleatorio
            const randomIndex = Math.floor(Math.random() * phrases.length);

            // Muestra la frase aleatoria
            phrases[randomIndex].style.display = 'block';

            // Configura un temporizador para mostrar la siguiente frase aleatoria después de un tiempo (ajusta el valor de tiempo según tu preferencia)
            setTimeout(() => {
                showRandomPhrases();
            }, 14000); // Cambia 5000 a la cantidad de milisegundos que desees entre frases
        }

        // Inicia la animación mostrando una frase aleatoria
        showRandomPhrases();


